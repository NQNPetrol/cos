    <!-- Calendar -->
    <div class="bg-zinc-800 border border-zinc-700 rounded-lg overflow-hidden">
        <!-- Calendar Controls -->
        <div class="p-4 border-b border-zinc-700 flex items-center justify-between">
            <button onclick="changeMonth(-1)" class="p-2 text-gray-400 hover:text-white hover:bg-zinc-700 rounded-lg transition">
