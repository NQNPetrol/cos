<script>
    const EVENTS_URL = '{{ route("client.calendario.eventos") }}';
    let currentDate = new Date();
    let events = [];
    const monthNames = ['Enero', 'Febrero', 'Marzo', 'Abril', 'Mayo', 'Junio', 'Julio', 'Agosto', 'Septiembre', 'Octubre', 'Noviembre', 'Diciembre'];

    function changeMonth(offset) {
        currentDate.setMonth(currentDate.getMonth() + offset);
        loadEvents();
    }

    function loadEvents() {
        fetch(EVENTS_URL, {
            headers: { 'Accept': 'application/json', 'X-Requested-With': 'XMLHttpRequest' }
        })
        .then(r => r.json())
        .then(data => {
            events = data;
            renderCalendar();
        })
        .catch(err => {
            console.error('Error loading events:', err);
            renderCalendar();
        });
    }

    function renderCalendar() {
        const year = currentDate.getFullYear();
        const month = currentDate.getMonth();
        document.getElementById('calendar-title').textContent = monthNames[month] + ' ' + year;

        const grid = document.getElementById('calendar-grid');
        grid.innerHTML = '';

        const firstDay = new Date(year, month, 1);
        // Monday = 0
        let startDay = firstDay.getDay() - 1;
        if (startDay < 0) startDay = 6;

        const daysInMonth = new Date(year, month + 1, 0).getDate();
        const today = new Date();

        // Total cells
        const totalCells = Math.ceil((startDay + daysInMonth) / 7) * 7;

        for (let i = 0; i < totalCells; i++) {
            const dayNum = i - startDay + 1;
            const cell = document.createElement('div');
            cell.className = 'min-h-[80px] md:min-h-[100px] border-b border-r border-zinc-700 p-1';

            if (dayNum >= 1 && dayNum <= daysInMonth) {
                const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(dayNum).padStart(2, '0')}`;
                const isToday = today.getFullYear() === year && today.getMonth() === month && today.getDate() === dayNum;

                cell.innerHTML = `<div class="text-xs font-medium ${isToday ? 'text-blue-400 font-bold' : 'text-gray-400'} mb-1">${dayNum}</div>`;

                if (isToday) {
                    cell.classList.add('bg-blue-900/10');
                }

                // Find events for this day
                const dayEvents = events.filter(e => {
                    const eDate = new Date(e.start);
                    return eDate.getFullYear() === year && eDate.getMonth() === month && eDate.getDate() === dayNum;
                });

                dayEvents.forEach(ev => {
                    const evDiv = document.createElement('div');
                    evDiv.className = 'text-xs p-1 mb-0.5 rounded cursor-pointer truncate';
                    evDiv.style.backgroundColor = ev.color + '30';
                    evDiv.style.color = ev.color;
                    evDiv.style.borderLeft = '3px solid ' + ev.color;
                    evDiv.textContent = ev.title;
                    evDiv.onclick = () => showEventDetail(ev);
                    cell.appendChild(evDiv);
                });
            } else {
                cell.classList.add('bg-zinc-900/50');
            }

            grid.appendChild(cell);
        }
    }

    function showEventDetail(ev) {
        const content = document.getElementById('event-detail-content');
        const props = ev.extendedProps || {};
        const date = new Date(ev.start);
        const dateFormatted = date.toLocaleDateString('es-AR', { weekday: 'long', day: '2-digit', month: 'long', year: 'numeric', hour: '2-digit', minute: '2-digit' });

        content.innerHTML = `
            <div class="bg-zinc-800 p-3 rounded-lg">
                <div class="text-xs text-gray-400">Tipo</div>
                <div class="text-gray-200 font-medium">${props.tipo_servicio === 'turno_service' ? 'Turno Service' : 'Turno Mecánico'}</div>
            </div>
            <div class="bg-zinc-800 p-3 rounded-lg">
                <div class="text-xs text-gray-400">Vehículo</div>
                <div class="text-gray-200 font-medium">${props.vehiculo || 'N/A'}</div>
            </div>
            <div class="bg-zinc-800 p-3 rounded-lg">
                <div class="text-xs text-gray-400">Taller</div>
                <div class="text-gray-200 font-medium">${props.taller || 'N/A'}</div>
            </div>
            <div class="bg-zinc-800 p-3 rounded-lg">
                <div class="text-xs text-gray-400">Fecha y Hora</div>
                <div class="text-gray-200 font-medium">${dateFormatted}</div>
            </div>
            <div class="bg-zinc-800 p-3 rounded-lg">
                <div class="text-xs text-gray-400">Estado</div>
                <div class="text-gray-200 font-medium capitalize">${ev.estado || 'N/A'}</div>
            </div>
            <div class="mt-2 p-2 bg-amber-900/20 border border-amber-800/30 rounded text-amber-400 text-xs flex items-center gap-2">
                <svg class="w-4 h-4 flex-shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"/></svg>
                Calendario de solo lectura. Contacte al administrador para modificar turnos.
            </div>
        `;
        document.getElementById('event-detail-modal').classList.remove('hidden');
    }

    // Load on init
    loadEvents();
</script>
@endsection
